
public class Swap1
{

	public static void main(String[] args) 
	{
	
		int num1=40;
		int num2=10;
		System.out.println("Befor swaping");
		System.out.println(num1);
		System.out.println(num2);
		//code to swap vale of both variable
		int num3;
		num3=num1;
		num1=num2;
		num2=num3;
		System.out.println("After swaping");
		System.out.println(num1);
		System.out.println(num2);
		
		

	}

}
